// Package errcode 全局统一错误码定义。
//
// 错误码按业务模块分号段管理，避免重复：
//   - 通用错误段：0（成功）/ 10000+
//   - 账号模块：20000+
//   - 游戏模块：30000+
//
// 各服务统一引用此包定义的错误码，不再各自维护散落常量。
package errcode

// ---- 通用错误段（0 / 10000+） ----

const (
	OK        = 0     // 成功
	ErrParam  = 10001 // 参数错误，客户端请求参数不合法
	ErrSystem = 10002 // 系统内部错误，服务端处理异常
)

// ---- 账号模块（20000+） ----

const (
	ErrUserExist    = 20001 // 用户名已存在，注册时用户名已被占用
	ErrUserNotExist = 20002 // 账号不存在，登录或查询时账号未注册
	ErrPassword     = 20003 // 密码错误，登录时密码验证不通过
	ErrNotLogin     = 20004 // 未登录，玩家未登录时尝试访问需要登录的接口
	ErrTokenInvalid = 20005 // Token 无效，Token 不存在、已过期或格式错误
)

// ---- 游戏模块（30000+） ----

const (
	ErrMatchFailed  = 30001 // 匹配失败，匹配队列异常或无法完成匹配
	ErrRoomNotExist = 30002 // 房间不存在，查询或操作了一个不存在的房间
	ErrGameNotStart = 30003 // 游戏未开始，游戏尚未开始即尝试操作
	ErrOpTooFast    = 30004 // 操作过于频繁，短时间内发送了过多操作请求
	ErrMatchTimeout = 30005 // 匹配超时，匹配等待时间超过上限
)
